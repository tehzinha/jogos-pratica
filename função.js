function soma(z,y){
    return z+y;
}

function subtracao(z,y){
    return z-y;
}

function multiplicacao(z,y){
    return z*y;
}

function divisao(z,y){
    return z/y;
}
console.log(soma(2,4));
console.log(subtracao(3,4));
console.log(multiplicacao(5,5));
console.log(divisao(2,2));
